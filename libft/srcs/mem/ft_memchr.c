#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((const char *)str)[i] == (char)c)
			return ((void *)(str + i));
		i++;
	}
	return (NULL);
}
